int		ft_atoi(char *str)
{
	int	res;
	int	x;
	int	coef;

	x = 0;
	res = 0;
	coef = 1;
	while ((str[x] > 8 && str[x] < 14) || str[x] == 32)
		x++;
	if (str[x] == '-' || str[x] == '+')
	{
		coef = (str[x] == '-' ? -1 : 1);
		x++;
	}
	while (str[x] >= '0' && str[x] <= '9')
	{
		res = (res * 10) + str[x] - '0';
		x++;
	}
	return (res * coef);
}
